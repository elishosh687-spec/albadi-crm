const BY_KEY = new Map<ObjectionKey, ObjectionPlay>(
  OBJECTION_PLAYBOOK.map((p) => [p.key, p])
);

/** The closed vocabulary the judge maps each objection to. */
export const OBJECTION_KEYS: ObjectionKey[] = OBJECTION_PLAYBOOK.map((p) => p.key);

export function getObjectionPlay(key: string | null | undefined): ObjectionPlay {
  return (key && BY_KEY.get(key as ObjectionKey)) || BY_KEY.get("other")!;
}
